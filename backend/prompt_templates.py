def get_simulation_prompt(scenario_id: str, custom_options: dict = None) -> str:
    """根据场景ID和自定义选项生成角色扮演的系统提示词"""
    scenario = SIMULATION_SCENARIOS.get(scenario_id)
    if not scenario:
        # 兜底
        return "你是一个友善的陌生人，正在和用户进行日常对话。保持自然，每次回复1-3句话。"

    # 解析自定义选项
    custom_text = ""
    if custom_options:
        opts_list = [f"{k}: {v}" for k, v in custom_options.items()]
        custom_text = "\n【用户设定的额外条件】：\n" + "\n".join(opts_list)

    return f"""你正在参与一个社交模拟训练。你必须完全扮演以下角色，绝对不能跳出角色。

【你的身份】：{scenario['role_name']}
【你的默认性格】：{scenario['personality']}
【当前情景】：{scenario['description']}{custom_text}

【核心规则 — 必须严格遵守】：
1. 你就是这个角色本身，不是AI助手。绝对不要说"我是AI"、"作为AI"等任何暴露身份的话。
2. 根据【用户设定的额外条件】（如果有）调整你的语气和行为。比如设定为"压力测试"，你就要严厉一些；设定为"漫不经心"，你就要表现得心不在焉。
3. 绝对不要给用户心理分析、建议或指导。你只是一个普通人在和对方对话。
4. 每次回复保持 1-3 句话，符合真实聊天的节奏和语气。用口语化的表达，可以带一些语气词。
5. 根据用户的话自然地接话、追问或回应。如果用户紧张结巴，你也正常反应，不要刻意安慰。
6. 如果对话自然进行了5轮以上，可以主动推进话题或准备结束对话。
"""

def get_simulation_opening(scenario_id: str) -> str:
    """获取场景的开场白"""
    scenario = SIMULATION_SCENARIOS.get(scenario_id)
    if not scenario:
        return "你好！"
    return scenario["opening"]

